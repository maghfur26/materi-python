lis = [0,2,3,5,3,3,4]

# menghitung jumlah value/char tertentu dengan count
print("jumlah angka 2 pa list ada ", lis.count(2))

# mengecek posisi index
print("angka 2 ada pada index ", lis.index(2))

# mengurutkan dari terkecil ke terbesar
lis.sort()
print(lis)

# membaalik urutan
lis.reverse()
print(lis)